@app.route('/api/analyze', methods=['POST'])
def analyze():
    data = request.json
    text = data.get('text', '').strip()
    if len(text) < 5:
        return jsonify({'error': 'Слишком короткий текст'}), 400

    category = category_model.predict([text])[0]
    cat_proba = float(max(category_model.predict_proba([text])[0]) * 100)

    priority = priority_model.predict([text])[0]
    pri_proba = float(max(priority_model.predict_proba([text])[0]) * 100)

    stats[category] += 1

    return jsonify({
        'category': category,
        'cat_confidence': round(cat_proba, 1),
        'priority': priority,
        'pri_confidence': round(pri_proba, 1),
        'stats': stats,
        'accuracy': round(accuracy * 100, 1),
        'deadline': '24 часа' if priority == 'Высокий' else '3–5 рабочих дней'
    })

@app.route('/api/stats')
def get_stats():
    return jsonify({'stats': stats, 'accuracy': round(accuracy * 100, 1)})

